package cursoAlura;

public class Telefone {

	private String estado;
	private int codigo;
	
	public Telefone(String estado, int codigo) {
		super();
		this.estado = estado;
		this.codigo = codigo;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public int getCodigo() {
		return codigo;
	}

	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	
	
	
	
}
